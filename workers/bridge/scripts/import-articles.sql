A comprehensive [2021 review in World Psychiatry](https://pmc.ncbi.nlm.nih.gov/articles/PMC8462234/) examined the current state of evidence across numerous psychiatric conditions:

**Depression**: The strongest evidence exists here. Higher levels of religious involvement consistently correlate with lower rates of depression and faster recovery from depressive episodes.

**Suicidality**: Religious involvement is associated with significantly lower rates of suicide attempts and completed suicides, even controlling for other factors.

**Substance Use**: Strong evidence shows that religious and spiritual practices protect against substance abuse and support recovery.

**Post-Traumatic Stress Disorder**: Spirituality and religiousness appear to buffer against post-traumatic stress, generally [increasing psychological growth](https://pubmed.ncbi.nlm.nih.gov/24702209/) following traumatic experiences.

**Anxiety**: Religious beliefs that emphasize a loving, supportive God correlate with lower anxiety; beliefs emphasizing punishment show more mixed results.

### The Self-Transcendence Factor

Perhaps the most illuminating line of research concerns [self-transcendence](https://pmc.ncbi.nlm.nih.gov/articles/PMC3810183/)—the psychological capacity to look beyond one''s own self and connect with something greater.

Psychologist [Pamela Reed](https://self-transcendence.org/theory) defines self-transcendence as "expansion of self-conceptual boundaries inwardly through introspective activities, outwardly through concerns about others'' welfare, and temporally through integration of the past and future." It is a personality trait, but one that can be cultivated.

Research distinguishes [three types of transcendence](https://www.frontiersin.org/journals/psychology/articles/10.3389/fpsyg.2020.547687/full):

1. **Ego transcendence**: Moving beyond self-preoccupation
2. **Self-transcendence**: Connection to others and the world
3. **Spiritual transcendence**: Connection to something beyond space and time

All three are associated with improved mental health outcomes, but spiritual transcendence appears particularly powerful in the face of severe adversity.

### Why Transcendence Heals

A 2023 paper titled "[Transcending the Self to Transcend Suffering](https://pmc.ncbi.nlm.nih.gov/articles/PMC10361767/)" proposed several mechanisms:

**Cognitive Reframing**: Transcendent experiences provide new frameworks for understanding suffering. What seems meaningless becomes part of a larger story.

**Emotional Regulation**: The profound positivity of transcendent experiences—awe, wonder, connectedness—can supersede and diminish negative emotions.

**Social Connection**: Spiritual communities provide belonging, support, and a sense of being part of something larger than oneself.

**Purpose and Meaning**: Perhaps most importantly, transcendence provides a sense of purpose that extends beyond personal concerns.

The ego, when left to its own devices, faces an impossible task: it must find meaning in a universe that appears indifferent to its existence, and it must confront the certainty of its own death. These are burdens too heavy for the self alone to bear.

### The Ego as Enemy

The ancient wisdom traditions have long warned that the ego—the small self that says "I" and "mine"—is both necessary and dangerous. Necessary because without it we could not function; dangerous because it tends to demand that the entire universe revolve around its needs.

Modern research suggests this insight has profound implications for mental health. Those who are [trapped in excessive self-focus](https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3810183/)—ruminating about their problems, comparing themselves to others, measuring their worth by external standards—are at higher risk for depression, anxiety, and prolonged grief.

Conversely, those who can step outside the ego—through meditation, through service to others, through religious devotion, or through simple experiences of awe—tend to be more resilient, more content, and better able to weather life''s storms.

### Faith and the Fear of Death

No aspect of the ego''s burden is heavier than the knowledge of mortality. [Terror Management Theory](https://www.thenewatlantis.com/publications/faith-and-the-fear-of-death), developed by social psychologists building on the work of Ernest Becker, proposes that much of human culture exists to manage the terror of death.

The relationship between religious belief and death anxiety is complex. Research consistently finds a [U-shaped curve](https://www.sciencedirect.com/science/article/abs/pii/S2352250X20301494):

- Those with **strong, consistent religious beliefs** report the lowest death anxiety
- Those with **no religious beliefs** report moderate death anxiety
- Those who are **moderately religious**—uncertain in their beliefs—report the highest death anxiety

What matters most is not the content of belief but its **consistency and depth**. The person who genuinely believes in an afterlife has made peace with mortality in a way that transforms the meaning of death. The atheist who has genuinely accepted the finality of death has also made peace, through a different route. It is those caught in between—half-believing, half-doubting—who suffer most.

This finding illuminates why halfhearted religion provides little benefit. The ego cannot be transcended through lukewarm commitment. One must, as the traditions say, give oneself wholly.

### Recent Advances (2024-2025)

The field continues to evolve rapidly. Recent research includes:

**Neuroimaging Studies**: [Brain scans during prayer and meditation](https://www.frontiersin.org/journals/psychology/articles/10.3389/fpsyg.2025.1568861/full) reveal distinct patterns of activity associated with transcendent experiences, lending biological credibility to subjective reports.

**Integration in Clinical Practice**: [Psychiatry is increasingly recognizing](https://www.psychiatrictimes.com/view/return-of-the-soul-the-role-of-spirituality-in-mental-health) that integrating spirituality into mental health care can foster deeper therapeutic connections and improved outcomes.

**Pediatric Applications**: [Research on children and adolescents](https://www.frontiersin.org/journals/psychiatry/articles/10.3389/fpsyt.2024.1472629/full) shows that religious involvement is associated with resilience after trauma, suggesting early spiritual development may be protective.

**Cross-Cultural Confirmation**: Studies across [diverse cultural contexts](https://www.frontiersin.org/journals/psychology/articles/10.3389/fpsyg.2024.1441560/full) confirm that the benefits of spiritual practice are not limited to Western Christianity but appear across religious traditions.

